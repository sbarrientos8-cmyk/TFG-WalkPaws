import UIKit

class Fonts
{
    static func figtreeMedium(_ size: CGFloat) -> UIFont
    {
        UIFont(name: "Figtree-Medium", size: size)!
    }

    static func figtreeRegular(_ size: CGFloat) -> UIFont
    {
        UIFont(name: "Figtree-Regular", size: size)!
    }
    
    static func figtreeLight(_ size: CGFloat) -> UIFont
    {
        UIFont(name: "Figtree-Light", size: size)!
    }
    
    static func plusJakartaSansLight(_ size: CGFloat) -> UIFont
    {
        UIFont(name: "PlusJakartaSans-Light", size: size)!
    }
    
    static func plusJakartaSansExtraBold(_ size: CGFloat) -> UIFont
    {
        UIFont(name: "PlusJakartaSans-ExtraBold", size: size)!
    }
}
